/* Tipos PRIMITIVOS */

/* Tipo Number */
/* É todo número, positivo ou negativo, inteiro ou quebrado */
let num1 = 10;
let num2 = 10.5;
let num3 = 0.10;
let num4 = -10;

/* Tipo BigInt */
/* É um número muito grande, é necessário o n no final do número */
let bigInt1 = 99999999999999999999999n;
let bigInt2 = 102215482321210478454n;

/* Tipo String */
/* São palavras, lista de caracteres */
let nome = 'Cassia'[2];
let sobrenome = "Sotolani"[5];
let frase = `Olá, meu nome é ${nome} ${sobrenome}`.length;

/* Tipo Boolean */
/* Valor lógico, verdadeiro ou falso, 0 ou 1 */
let maiorIdade = true;
let menorIdade = false;

/* Tipo Null */
/* Valor é definido, o valor existe mas o valor é vazio */
let idade = null;

