/* Operadores Lógicos */
/* E ou AND */
/* Só é verdadeiro se ambas as partes forem verdadeiras */
true && true === true;
true && false === false;
false && true === false;
false && false === false;

/* OU ou OR */
/* Só é false se ambas as partes forem falsas */
true || true === true;
true || false === true;
false || true === true;
false || false === false;

/* NÃO ou NOT */
/* Inverte o resultado - de verdadeiro para falso e vice-versa */
!true === false;
!false === true;