/* Operadores Atribuição */
/* Atribuição - atribui um valor a uma variável */
let nome = 'Cassia';

/* Mais igual - soma o valor que já tem na variável com o outro valor */
let a = 5;
a += 4;

/* Menos igual - subtrai o valor que já tem na variável com o outro valor */
let b = 5;
b -= 4;

/* Multiplica igual - multiplica o valor que já tem na variável com o outro valor */
let c = 5;
c *= 4;

/* Divide igual - divide o valor que já tem na variável com o outro valor */
let d = 5;
d /= 4;

/* MODE igual - divide o valor da variável pelo outro valor e adiciona o resto na variável */
let e = 15;
e %= 4;

/* Incrementar - soma +1 na variável */
let f = 21;
/* mostra o valor antes do incremento */
f++
/* mostra o valor já com o incremento */
++f

/* Decrementar - subtrai -1 na variável */
let g = 21;
/* mostra o valor antes do decremento */
g--
/* mostra o valor já com o decremento */
--g