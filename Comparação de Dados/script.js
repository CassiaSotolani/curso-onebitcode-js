/* Comparação de Dados */
/* Igualdade comparando valores */
3 == '3'; // retorno é verdadeira

/* Igualdade comparando valores e tipos */
3 === '3'; // retorno é falso

/* Diferente comparando valores */
4 != '4'; // retorno é falso

/* Diferente comparando valores e tipos */
4 !== '4'; // retorno é verdadeiro

/* Maior que */
4 > 3; // retorno é verdadeiro

/* Maior igual que */
4 >= 5; // retorno é falso

/* Menor que */
3 < 2; // retorno é falso

/* Menor igual que */
5 <= 10 // retorno é verdadeiro